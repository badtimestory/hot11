package com.shop.s1.detailOrder;

public class DetailOrderDTO {
	private Long d_num;
	private Long o_num;
	private String m_id;
	private Long p_num;
	private Long t_num;
	private Long d_amount;
	
	public Long getD_num() {
		return d_num;
	}
	public void setD_num(Long d_num) {
		this.d_num = d_num;
	}
	public Long getO_num() {
		return o_num;
	}
	public void setO_num(Long o_num) {
		this.o_num = o_num;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
	public Long getP_num() {
		return p_num;
	}
	public void setP_num(Long p_num) {
		this.p_num = p_num;
	}
	public Long getT_num() {
		return t_num;
	}
	public void setT_num(Long t_num) {
		this.t_num = t_num;
	}
	public Long getD_amount() {
		return d_amount;
	}
	public void setD_amount(Long d_amount) {
		this.d_amount = d_amount;
	}
	
	
}
